SVGEN_Handle SVGEN_init(void *pMemory, const size_t numBytes)
{
    SVGEN_Handle svgenHandle;

    if (numBytes < sizeof(SVGEN_Obj))
        return ((SVGEN_Handle)NULL);

    // 分配句柄
    svgenHandle = (SVGEN_Handle)pMemory;

    return (svgenHandle);
} // end of SVGEN_init()

